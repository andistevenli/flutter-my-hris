class PersonalDTO {
  final String fullName;
  final String employeeCode;
  final String phoneNumber;
  final String email;
  final String gender;
  final String religion;
  final String maritalStatus;
  final String bloodType;
  final String dateOfBirth;
  final String placeOfBirth;

  PersonalDTO({
